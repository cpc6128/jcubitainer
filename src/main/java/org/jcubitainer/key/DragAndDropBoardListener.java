package org.jcubitainer.key;

import java.awt.datatransfer.DataFlavor;
import java.awt.datatransfer.UnsupportedFlavorException;
import java.awt.dnd.DropTargetDragEvent;
import java.awt.dnd.DropTargetDropEvent;
import java.awt.dnd.DropTargetEvent;
import java.awt.dnd.DropTargetListener;
import java.io.File;
import java.io.IOException;
import java.util.List;

import org.jcubitainer.display.theme.ThemeManager;

public class DragAndDropBoardListener implements DropTargetListener {

    /**
     * 
     */
    public DragAndDropBoardListener() {
        super();
    }

    /* (non-Javadoc)
     * @see java.awt.dnd.DropTargetListener#dragEnter(java.awt.dnd.DropTargetDragEvent)
     */
    public void dragEnter(DropTargetDragEvent dtde) {
    }

    /* (non-Javadoc)
     * @see java.awt.dnd.DropTargetListener#dragExit(java.awt.dnd.DropTargetEvent)
     */
    public void dragExit(DropTargetEvent dte) {
    }

    /* (non-Javadoc)
     * @see java.awt.dnd.DropTargetListener#dragOver(java.awt.dnd.DropTargetDragEvent)
     */
    public void dragOver(DropTargetDragEvent dtde) {
    }

    /* (non-Javadoc)
     * @see java.awt.dnd.DropTargetListener#drop(java.awt.dnd.DropTargetDropEvent)
     */
    public void drop(DropTargetDropEvent dtde) {
        // http://forum.clubic.com/forum2.php3?post=13326&cat=13
        DataFlavor[] tab = dtde.getCurrentDataFlavors();
        if (tab[0].isFlavorJavaFileListType())
                try {
                    dtde.acceptDrop(dtde.getDropAction());
                    File f = (File) ((List) dtde.getTransferable()
                            .getTransferData(dtde.getCurrentDataFlavors()[0]))
                            .get(0);
                    ThemeManager.addThemeFromDragAndDropAndSaveToDisk(f);
                } catch (UnsupportedFlavorException e) {
                    e.printStackTrace();
                } catch (IOException e) {
                    e.printStackTrace();
                }
        dtde.dropComplete(true);
    }

    /* (non-Javadoc)
     * @see java.awt.dnd.DropTargetListener#dropActionChanged(java.awt.dnd.DropTargetDragEvent)
     */
    public void dropActionChanged(DropTargetDragEvent dtde) {
    }
}